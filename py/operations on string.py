# String slicing
fruit = "Apple"
print(fruit[0:4])   #Give according to indexes
print(fruit[:3])    #Give from 0 to 3
print(fruit[:])     #Give all charcters

print(fruit[0:-3])  #it minus 3 from from total lenght...That make index from 0 to 2 like [0:2]
print(fruit[-3:-1])  #it minus both from from total lenght...That make index from 2 to 4 like [2:4]